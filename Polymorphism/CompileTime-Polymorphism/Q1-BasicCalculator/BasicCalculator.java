class BasicCalculator {
	public static String addition(int a,int b) {
		return "Addition of : " + a + " + " + b + " is " + (a+b); 
	}

	public static String addition(int a,int b,int c) {
		return "Addition of : " + a + " + " + b + " + " + c + " is " + (a+b+c); 
	}

	public static String addition(double a,double b) {
		return "Addition of : " + a + " + " + b + " is " + (a+b); 
	}	

	public static String addition(double a,double b,double c) {
		return "Addition of : " + a + " + " + b + " + " + c + " is " + (a+b+c); 
	}

}