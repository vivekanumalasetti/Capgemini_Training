class LogicalOperator {
	public static void main(String[] args) {

		boolean i = true;

		System.out.println("The value of i is : " + i);


		boolean j = false;

		System.out.println("The value of j is : " + j);

		boolean ans1 = i && j;

		System.out.println("The value of ans is : " + ans1);

		boolean ans2 = i || j;

		System.out.println("The value of ans is : " + ans2);

		boolean ans3 = !j;

		System.out.println("The value of ans is : " + ans3);


	}
}