class RelationalOperation {
	public static void main(String[] args) {

		int i = 10;

		System.out.println("The value of i is : " + i);

		int j = 5;

		System.out.println("The value of j is : " + j);

		boolean ans1 = i > j;

		System.out.println("is " + i + " > " + j + " : " + ans1);

		boolean ans2 = i < j;

		System.out.println("is " + i + " < " + j + " : " + ans2);

		boolean ans3 = i == j;

		System.out.println("is " + i + " == " + j + " : " +  ans3);

		boolean ans4 = i != j;

		System.out.println("is " + i + " != " + j + " : " +  ans4);

		boolean ans5 = i >= j;

		System.out.println("is " + i + " >= " + j + " : " +  ans5);

		boolean ans6 = i <= j;

		System.out.println("is " + i + " <= " + j + " : " +  ans6);


	}
}