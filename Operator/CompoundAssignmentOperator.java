class CompoundAssignmentOperator {
	public static void main(String[] args) {

		int a = 10;

		System.out.println("The value of a is : " + a);


		a += 10;

		System.out.println("The value of a is : " + a);

		a -= 10;

		System.out.println("The value of a is : " + a);	

		a *= 10;

		System.out.println("The value of a is : " + a);

		a /= 10;

		System.out.println("The value of a is : " + a);	

		a %= 10;

		System.out.println("The value of a is : " + a);					
	}
}