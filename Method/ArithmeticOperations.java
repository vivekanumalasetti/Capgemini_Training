class ArithmeticOperations{
	public static void addition(int input1, int input2) {

		System.out.println("A is : " + input1);
		System.out.println("B is : " + input2);

		int add = input1 + input2;

		System.out.println("The sum of " + input1 + " + " + input2 + " is : " + add);
		System.out.println();
		
	}

	public static void subpraction(int input1, int input2) {

		System.out.println("A is : " + input1);
		System.out.println("B is : " + input2);	

		int sub = input1 - input2;

		System.out.println("The sum of " + input1 + " - " + input2 + " is : " + sub);
		System.out.println();
		
	}

	public static void multiplation(int input1, int input2) {

		System.out.println("A is : " + input1);
		System.out.println("B is : " + input2);	

		int mul = input1 * input2;

		System.out.println("The sum of " + input1 + " * " + input2 + " is : " + mul);
		System.out.println();
		
	}

	public static void divion(int input1, int input2) {

		System.out.println("A is : " + input1);
		System.out.println("B is : " + input2);	

		int div = input1 / input2;

		System.out.println("The sum of " + input1 + " * " + input2 + " is : " + div);
		System.out.println();
		
	}

	public static void modulas(int input1, int input2) {

		System.out.println("A is : " + input1);
		System.out.println("B is : " + input2);	

		int mod = input1 % input2;

		System.out.println("The sum of " + input1 + " * " + input2 + " is : " + mod);
		System.out.println();
		
	}
}