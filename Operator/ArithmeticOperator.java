class ArithmeticOperator {
	public static void main(String[] args) {

		int i = 50;

		int j = 10;

		System.out.println("The value of i is : " + i);

		System.out.println("The value of j is : " + j);

		int ans1 = i + j;

		System.out.println("The value of answer of " + i + " + " + j + " is : " + ans1);		

		int ans2 = i - j;

		System.out.println("The value of answer of " + i + " - " + j + " is : " + ans2);


		int ans3 = i * j;

		System.out.println("The value of answer of " + i + " * " + j + " is : " + ans3);


		int ans4 = i / j;

		System.out.println("The value of answer of " + i + " / " + j + " is : " + ans4);		


		int ans5 = i % j;

		System.out.println("The value of answer of " + i + " % " + j + " is : " + ans5);
	}
}